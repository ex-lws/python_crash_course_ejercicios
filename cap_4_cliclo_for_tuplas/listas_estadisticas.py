#hay varias funciones que podemos usar para poder econtrar valores en una lista dependiendo 
#nuestras necesidades

#los valores mas altos, minimos, etc

lista = [] #lista vacia

#Le agragamos valores con .append(valor)

lista.append (1)
lista.append (33)
lista.append (50)
lista.append (0)

print (lista)

#max (nombreLista) VALOR MAS ALTO
#min (nombreLista) VALOR MAS BAJO
#len (nombreLista) CANTIDAD DE ELEMENTOS
#sum (nombreLista() SUMA DE TODOS LOS ELEMENTOS EN LA LISTA

print (max (lista)) #max () imrpime el valor mas alto de la lista

print (min (lista)) ## min imprime el valor minimo de la lista o el mas bajo

print (len(lista)) #Cantidad de elementos de la lista.

print (sum(lista)) #suma todos los valores de la lista
