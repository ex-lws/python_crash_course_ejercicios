#El operador modulo lo que hace no es regresarnos el resultado de la division, sino el residuo

#Veamos los ejemplos: 

resultado_division = 6 / 3 #Aqui si vemos cuanto da 6 entre 3 = 2
print (resultado_division)

resultado_modulo_1 = 6 % 3 #El residuo el 0
print (resultado_modulo_1)

resultado_modulo_2 = 4 % 3 #El residuo es 1
print (resultado_modulo_2)

#El operador de modulo nos recuerda a como funcionan los numeros primos...



