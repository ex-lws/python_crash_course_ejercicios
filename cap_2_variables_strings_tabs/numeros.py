#Trabajando con numeros, hay algunos operadores que podemos usar, suma, resta, multiplicacion y division

#Los enteros son numeros sin decimales y los float aquellos con decimales.

#Operando y mandando los resultados a variables.

resultado_suma = 2 + 5 #resultado_suma = 7

resultado_resta = 100 - 27

resultado_multiplicacion = 3 * 8

resultado_division = 10 / 2

resultado_modulo = 6 % 3 

#podemos desde la consola hacer una operacion, no requiere de colocar comillas ni 
#nada por el estilo

print (resultado_suma, resultado_resta, resultado_multiplicacion, resultado_division, resultado_modulo)

#Podemos realizar operaciones directamente desde la consola

print (2+5)
print (200 + 21)

#Podemos usar potencias con el operador **

resultado_potencia = 3 ** 3 #3 elevado a la 3

resultado_potencia_2 = 2 ** 1 #2 elevado a la 1

print (resultado_potencia, resultado_potencia_2)
print (3 ** 2)

#Por defecto python usa la jerarquia de operaciones, pero podemos usar 
#parentesis para especificar

operacion = 5 + 3 * 2 #11

operacion_parentesis = (5 + 3) * 2 #16, primero hacer la suma y despues...

print (operacion, operacion_parentesis)

#Los flotantes son numeros que utilizan el punto decimal y de igual manera usan 
#los demas operadores

suma_flotante = 0.1 + 0.4

resta_flotante = 0.7 - 0.1

multiplicacion_flotante = 0.5 * 2

division_flotante = 2.0 / 4

potencia_flotante = 0.2 ** 2

modulo_residuo_division = 3%3

#Todas las operaciones y sus resultados en este caso se almacenan a una 
#variable ya determinada, ideal para almacenarla.

print (suma_flotante, resta_flotante, multiplicacion_flotante, division_flotante, potencia_flotante, modulo_residuo_division)

